function play1() {
    let audio1 = document.getElementById("audio1");

    if (audio1.paused) {
        audio1.play();
        document.getElementById("audioImg").src = "resource/about/audio.png";
    } else {
        audio1.pause();
        audio1.currentTime = 0;
        document.getElementById("audioImg").src = "resource/about/audio.png"
    }
}

